using System;
namespace csharp.net
{
    public class switchStatement
    {

        public switchStatement()
        {
            int number = 1;

            switch (number)
            {
                case 0:
                    Console.WriteLine("The number is Zero!");
                    break;
                case 1:
                    Console.WriteLine("The number is one!");
                    break;

            }
        }
    }
}
